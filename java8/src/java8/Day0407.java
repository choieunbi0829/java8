package java8;

public class Day0407 {
	public static void main(String[] args) {
		int a = 8;
		int b = 5;
		
		int res1 = a + b;
		int res2 = a - b;
		int res3 = a * b;
		int res4 = a / b;
		int res5 = a % b;
		
		System.out.println("a+b의 값은" + res1);
		System.out.println("a-b의 값은" + res2);
		System.out.println("a*b의 값은" + res3);
		System.out.println("a/b의 값은" + res4);
		System.out.println("a/b의 나머지값은" + res5);
		
		}
}
